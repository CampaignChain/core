namespace CampaignChain\CoreBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * @ORM\Entity
 * @ORM\Table(name="campaignchain_theme")
 */
class Theme extends Meta
{
    const STORAGE_PATH = 'theme';

    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @ORM\Column(type="string")
     */
    protected $name;

    /**
     * @Assert\Image(
     *     mimeTypes = "image/x-icon",
     *     minWidth = 16,
     *     minHeight = 16,
     *     maxWidth = 32,
     *     maxHeight = 32,
     *     allowLandscape = false,
     *     allowPortrait = false
     * )
     * @ORM\Column(type="string")
     */
    protected $favicon;

    /**
     * @Assert\Image(
     *     mimeTypes = "image/png",
     *     maxWidth = 260,
     *     maxHeight = 48,
     *     allowPortrait = false
     * )
     * @ORM\Column(type="string")
     */
    protected $logo;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getFavicon()
    {
        return $this->favicon;
    }

    /**
     * @param mixed $favicon
     */
    public function setFavicon($favicon)
    {
        $this->favicon = $favicon;
    }

    /**
     * @return mixed
     */
    public function getLogo()
    {
        return $this->logo;
    }

    /**
     * @param mixed $logo
     */
    public function setLogo($logo)
    {
        $this->logo = $logo;
    }
}